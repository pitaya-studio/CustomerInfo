package com.trekiz.admin.modules.mtourfinance.pojo;

public class OverseasAccount {

	private String accountName;//:'开户名',
	private String routing;//:'Routing',
	private String account;//:'账号',
	private String swiftNumber;//:'Swift number'
	private String accountBank;//:'开户行',
	private String accountBankAddress;//:'开户行地址',
	private String accountPhone;//:'开户手机号'
	public String getAccountName() {
		return accountName;
	}
	public void setAccountName(String accountName) {
		this.accountName = accountName;
	}
	public String getRouting() {
		return routing;
	}
	public void setRouting(String routing) {
		this.routing = routing;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getSwiftNumber() {
		return swiftNumber;
	}
	public void setSwiftNumber(String swiftNumber) {
		this.swiftNumber = swiftNumber;
	}
	public String getAccountBank() {
		return accountBank;
	}
	public void setAccountBank(String accountBank) {
		this.accountBank = accountBank;
	}
	public String getAccountBankAddress() {
		return accountBankAddress;
	}
	public void setAccountBankAddress(String accountBankAddress) {
		this.accountBankAddress = accountBankAddress;
	}
	public String getAccountPhone() {
		return accountPhone;
	}
	public void setAccountPhone(String accountPhone) {
		this.accountPhone = accountPhone;
	}
	
	
}
