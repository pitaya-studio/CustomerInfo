package com.trekiz.admin.modules.mtourfinance.pojo;
/**
 * 航班信息
 * @author zhaohaiming
 * */
public class Flights {

	public String flightNo;//:'航班号',
	public String departureTime;//:'出发时间',//包含日期
	public String arrivalTime;//:'到达时间',
	public String departureAirport;//:'出发机场',
	public String arrivalAirport;//:'到达机场',
	public String memo;//:'备注'
	public String getFlightNo() {
		return flightNo;
	}
	public void setFlightNo(String flightNo) {
		this.flightNo = flightNo;
	}
	public String getDepartureTime() {
		return departureTime;
	}
	public void setDepartureTime(String departureTime) {
		this.departureTime = departureTime;
	}
	public String getArrivalTime() {
		return arrivalTime;
	}
	public void setArrivalTime(String arrivalTime) {
		this.arrivalTime = arrivalTime;
	}
	public String getDepartureAirport() {
		return departureAirport;
	}
	public void setDepartureAirport(String departureAirport) {
		this.departureAirport = departureAirport;
	}
	public String getArrivalAirport() {
		return arrivalAirport;
	}
	public void setArrivalAirport(String arrivalAirport) {
		this.arrivalAirport = arrivalAirport;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	
	
}
